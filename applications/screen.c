struct hm_print_data {
    rt_uint16_t x;
    rt_uint16_t y;
    const char* str;
};

rt_err_t hm_print(struct hm_print_data* pd)
{
    if(RT_NULL == print_mb) {
        PRINT_DBG("print mailbox is NULL\n");
        return -RT_EEMPTY;
    }

    return rt_mb_send_wait(print_mb, (rt_uint32_t)pd, PRINT_SEND_DELAY);
}

static void hm_screen_first_image(void)
{
    BSP_EPD_DrawImage(0,0,172,72,(uint8_t*)logo);
    BSP_EPD_RefreshDisplay();
    BSP_EPD_DrawImage(0,0,172,72,(uint8_t*)logo);
    BSP_EPD_RefreshDisplay();
}

void hm_print_thread_entry(void* parameter)
{
    struct hm_print_data* recv_data;
    print_mb = rt_mb_create("printmb", PRINT_MAILBOX_MAX_NUM, RT_IPC_FLAG_FIFO);
    RT_ASSERT(print_mb != RT_NULL);
    hm_screen_first_image();

    while(1) {
        if(rt_mb_recv(print_mb, (rt_uint32_t*)&recv_data, RT_WAITING_FOREVER)==RT_EOK) {
            RT_ASSERT(recv_data);
            RT_ASSERT(recv_data->str);
            BSP_EPD_DisplayStringAt((uint16_t)recv_data->x, (uint16_t)recv_data->y, 
                                    (uint8_t*)recv_data->str, LEFT_MODE);
        }
        if(print_mb->entry==0) {
            BSP_EPD_RefreshDisplay();
        }
    }
}
